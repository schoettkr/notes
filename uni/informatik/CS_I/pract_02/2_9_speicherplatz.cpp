#include <iostream>

using namespace std;

int main()
{
  cout << "char size in byte: " << sizeof(char) << endl;
  cout << "int size in byte: " << sizeof(int) << endl;
  cout << "long size in byte: " << sizeof(long) << endl;
  cout << "float size in byte: " << sizeof(float) << endl;
  cout << "double size in byte: " << sizeof(double) << endl;

  return 0;
}
